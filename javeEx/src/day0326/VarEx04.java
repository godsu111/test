package day0326;

public class VarEx04 {
	public static void main(String[] args) {
		// 정수형 기본 int 
		int num;
		num = 3;
		System.out.println("변수 num의 값은 "+num+"입니다."); //공백도 문자이다 " " 안에서 적용
		
		num = 5;
		System.out.println("변수 num의 값을 변경하였습니다.");
		System.out.println("변수 num의 값은 "+num+"입니다.");
		
	}
}
