package day0326;

public class VarEx11 {
	public static void main(String[] args) {
		// 논리형 boolean - ture / false의 값만 들어간다.
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println(b1);
		System.out.println(b2);

		System.out.println(3>4);
		System.out.println(3<4);
		
		int x = 5;
		int y = 10;
		System.out.println(x>y);
		System.out.println(x<y);
		
		boolean z = x>y;
		System.out.println("z의 결과 : "+z);
		
	}
}
















