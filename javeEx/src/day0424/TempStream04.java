package day0424;

public class TempStream04 {
	public static void main(String[] args) {
		System.out.println("H\ne\nl\nl\no\n!"); // \n 줄바꿈
		System.out.println("H\te\tl\tl\to\t!");	// \t 들여쓰기
		System.out.println("\"Hello\"");		// \" 큰따옴표 출력
		System.out.println("\'hello\'");		// \' 작은따옴표 출ㄹ력
		System.out.println("\\hello\\");  		// \ 역슬래시 출력 단 \\두개 사용해야함
	}
}
